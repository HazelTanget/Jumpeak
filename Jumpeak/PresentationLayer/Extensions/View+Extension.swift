import SwiftUI

extension Text {
    /// Font with 28 size
    func xlFont(weight: Font.Weight = .regular) -> some View {
        self
            .font(.system(size: 28, weight: weight))
    }
    
    /// Font with 20 size
    func lFont(weight: Font.Weight = .regular) -> some View  {
        self
            .font(.system(size: 20, weight: weight))
    }
    
    /// Font with 16 size
    func mFont(weight: Font.Weight = .regular) -> some View  {
        self
            .font(.system(size: 16, weight: weight))
    }
    
    /// Font with 12 size
    func sFont(weight: Font.Weight = .regular) -> some View  {
        self
            .font(.system(size: 12, weight: weight))
    }
}

